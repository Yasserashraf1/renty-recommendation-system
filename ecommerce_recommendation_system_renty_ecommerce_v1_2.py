def get_recommendations_for_input_user(user_id_input, model, dataset, user_features, item_features, df, n_recommendations=10):
    """
    Takes a user ID input and provides recommendations using the loaded LightFM model.
    Handles cold start problem by recommending popular items for new users.

    Args:
        user_id_input: The CustomerKey of the user for whom to generate recommendations.
        model: The trained LightFM model.
        dataset: The LightFM Dataset object.
        user_features: The user features matrix.
        item_features: The item features matrix.
        df: The original pandas DataFrame containing user purchase history.
        n_recommendations: The number of recommendations to generate.
    """
    print(f"\nAttempting to get recommendations for user ID: {user_id_input}")

    try:
        user_id_input = int(user_id_input)
    except ValueError:
        print(f"Invalid user ID format: {user_id_input}. User ID should be an integer.")
        return

    # Get user mapping to check if user exists
    user_id_map, _, _, _ = dataset.mapping()

    # Check if user exists in training data
    if user_id_input not in user_id_map:
        print(f"\n User {user_id_input} is a NEW USER (not in training data)")
        print(" Applying COLD START STRATEGY: Recommending most popular items...\n")

        # Get most popular items based on various popularity metrics
        item_popularity = df.groupby('ProductKey').agg({
            'OrderQuantity': 'sum',  # Total quantity sold
            'CustomerKey': 'nunique',  # Number of unique customers
            'OrderNumber': 'count'  # Number of orders
        }).reset_index()

        item_popularity.columns = ['ProductKey', 'TotalQuantitySold', 'UniqueCustomers', 'OrderCount']

        # Calculate composite popularity score
        # Normalize each metric to 0-1 range
        item_popularity['NormQuantity'] = (item_popularity['TotalQuantitySold'] - item_popularity['TotalQuantitySold'].min()) / \
                                           (item_popularity['TotalQuantitySold'].max() - item_popularity['TotalQuantitySold'].min())
        item_popularity['NormCustomers'] = (item_popularity['UniqueCustomers'] - item_popularity['UniqueCustomers'].min()) / \
                                            (item_popularity['UniqueCustomers'].max() - item_popularity['UniqueCustomers'].min())
        item_popularity['NormOrders'] = (item_popularity['OrderCount'] - item_popularity['OrderCount'].min()) / \
                                         (item_popularity['OrderCount'].max() - item_popularity['OrderCount'].min())

        # Weighted popularity score (40% quantity, 30% customers, 30% orders)
        item_popularity['PopularityScore'] = (0.4 * item_popularity['NormQuantity'] +
                                              0.3 * item_popularity['NormCustomers'] +
                                              0.3 * item_popularity['NormOrders'])

        # Sort by popularity and get top N
        top_popular_items = item_popularity.nlargest(n_recommendations, 'PopularityScore')

        # Create recommendations dataframe with product details
        recommendations = []
        for idx, row in top_popular_items.iterrows():
            product_key = row['ProductKey']
            item_info = df[df['ProductKey'] == product_key].iloc[0]

            recommendations.append({
                'Rank': len(recommendations) + 1,
                'ProductKey': product_key,
                'ModelName': item_info['ModelName'],
                'ProductDescription': item_info['ProductDescription'][:60] + '...',
                'PopularityScore': f"{row['PopularityScore']:.4f}",
                'TotalSold': int(row['TotalQuantitySold']),
                'UniqueCustomers': int(row['UniqueCustomers'])
            })

        recommendations_df = pd.DataFrame(recommendations)

        print(f" Top {n_recommendations} POPULAR ITEMS for new user:")
        print("=" * 100)
        display(recommendations_df)
        print("\n TIP: These are the most popular items across all customers.")
        print("   Once this user makes purchases, personalized recommendations will be available!\n")

        return recommendations_df

    # User exists in training data - use personalized recommendations
    recommendations = get_recommendations(
        model, user_id_input, dataset, user_features, item_features,
        df, n_recommendations=n_recommendations, filter_already_purchased=True
    )

    if recommendations is not None and not recommendations.empty:
        print(f"\n PERSONALIZED Recommendations for user {user_id_input}:")
        print("=" * 100)
        display(recommendations)
    elif recommendations is not None and recommendations.empty:
        print(f"\n No new recommendations found for user {user_id_input}")
        print("   (User has already purchased all highly-ranked items)")
        print("\n Falling back to POPULAR ITEMS not yet purchased...\n")

        # Fall back to popular items the user hasn't purchased yet
        purchased_items = set(df[df['CustomerKey'] == user_id_input]['ProductKey'].unique())

        item_popularity = df.groupby('ProductKey').agg({
            'OrderQuantity': 'sum',
            'CustomerKey': 'nunique',
            'OrderNumber': 'count'
        }).reset_index()

        item_popularity.columns = ['ProductKey', 'TotalQuantitySold', 'UniqueCustomers', 'OrderCount']

        # Filter out purchased items
        item_popularity = item_popularity[~item_popularity['ProductKey'].isin(purchased_items)]

        # Calculate popularity score
        item_popularity['NormQuantity'] = (item_popularity['TotalQuantitySold'] - item_popularity['TotalQuantitySold'].min()) / \
                                           (item_popularity['TotalQuantitySold'].max() - item_popularity['TotalQuantitySold'].min())
        item_popularity['NormCustomers'] = (item_popularity['UniqueCustomers'] - item_popularity['UniqueCustomers'].min()) / \
                                            (item_popularity['UniqueCustomers'].max() - item_popularity['UniqueCustomers'].min())
        item_popularity['NormOrders'] = (item_popularity['OrderCount'] - item_popularity['OrderCount'].min()) / \
                                         (item_popularity['OrderCount'].max() - item_popularity['OrderCount'].min())

        item_popularity['PopularityScore'] = (0.4 * item_popularity['NormQuantity'] +
                                              0.3 * item_popularity['NormCustomers'] +
                                              0.3 * item_popularity['NormOrders'])

        top_popular_items = item_popularity.nlargest(n_recommendations, 'PopularityScore')

        fallback_recommendations = []
        for idx, row in top_popular_items.iterrows():
            product_key = row['ProductKey']
            item_info = df[df['ProductKey'] == product_key].iloc[0]

            fallback_recommendations.append({
                'Rank': len(fallback_recommendations) + 1,
                'ProductKey': product_key,
                'ModelName': item_info['ModelName'],
                'ProductDescription': item_info['ProductDescription'][:60] + '...',
                'PopularityScore': f"{row['PopularityScore']:.4f}"
            })

        fallback_df = pd.DataFrame(fallback_recommendations)
        print(" Popular items you haven't purchased yet:")
        display(fallback_df)

    return recommendations


